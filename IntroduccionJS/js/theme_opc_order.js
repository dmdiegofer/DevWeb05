/* OPERATION ORDER 

The order of math operations has the same rules than arithmetics
*/
let math = 20 + 60 * 2 ;

/* The parentheses have priority as well */
let math_ = (20 + 60) * 2 ;

/* INCREMENTS */

let points = 0 ;
// plus one
points ++ ;
++ points ;
// less one
points -- ;
-- points ;
// plus and less N
points += 4;
points -= 6;
